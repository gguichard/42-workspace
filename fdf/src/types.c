#include <math.h>
#include "fdf.h"

void	iso(t_pos *pos, int *x, int *y)
{
	*x = (pos->x - pos->y) * cos(0.523599);
	*y = -(pos->z) + (pos->x + pos->y) * sin(0.523599);
}
